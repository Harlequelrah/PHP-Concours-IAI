                <h3>FORMATION MODULAIRE</h3>
                Une formation modulaire est une formation dont le programme peut être décomposé en un nombre déterminé de modules.
                un module est une partie de la formation qui présente suffisamment de cohérence pour en constituer une partie détachable. La cohérence tient au fait que le module est
                composé d’objectifs propres identifiés, d’un contenu spécifique, d’une évaluation et qu’il vise une compétence ou un ensemble de compétences liées. Le but de cette formation est de fournir aux participants les compétences nécessaires pour réaliser des projets web, multimédia et graphiques de manière professionnelle.
                Les participants peuvent choisir les modules qui correspondent à leurs intérêts et à leurs objectifs de carrière,
                tout en construisant leur parcours de formation personnalisé.
