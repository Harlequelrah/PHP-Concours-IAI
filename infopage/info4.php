                <h3>FORMATION CERTIFIANTE</h3>
                A côté des diplômes bien connus de tous, il existe des formations très opérationnelles et généralement plus courtes qui débouchent sur un certificat ou un titre professionnel.
                Mais qu’est ce qu’une formation certifiante ? Souvent moins chères que les formations diplômantes,
                les formations certifiantes sont appréciées des recruteurs et très valorisées sur un CV car leur enseignement est en lien direct avec les besoins des entreprises d’une branche donnée.
                C’est là leur force, mais aussi leur faiblesse car de ce fait elles restent dédiées à un métier précis.
            </p>
